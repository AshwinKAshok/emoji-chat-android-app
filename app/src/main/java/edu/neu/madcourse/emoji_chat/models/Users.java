package edu.neu.madcourse.emoji_chat.models;

public class Users {
    String name;
    String count;

    public Users() {
    }

    public Users(String name, String count) {
        this.name = name;
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }
}
